    <details>
        <summary>Ça veut dire quoi, éco-responsable ?</summary>
        <p>
            La notion d'écoresponsable consiste en l'adoption de gestes permettant de limiter l'empreinte écologique et
            son impact environnemental. Cette attitude peut être adoptée par les personnes physiques, mais aussi les
            entreprises et organismes. Il revient à chacun de protéger l'environnement et de lutter contre le
            réchauffement climatique en privilégiant des conduites écologiques. En plus, il est possible d'être
            consommateur ou producteur éco-responsable à la maison, au travail, dans les transports etc...
        </p>
    </details>

    <details>
        <summary>Je suis particulier, comment rendre mon quotidien plus éco-responsable ?</summary>
        <p>
            Pour devenir plus écolo au quotidien, vous devez apprendre à réduire au maximum votre production de déchets.
            Cela commence par une réduction des achats et leur utilisation pour non seulement préserver la planète, mais
            faire également des économies considérables ! Mais il existe énormément d’autres manières d’adopter un mode
            de vie éco-responsable, vous pourrez vous informez sur toutes les bonnes pratiques via notre article juste
            ici.
        </p>
    </details>

